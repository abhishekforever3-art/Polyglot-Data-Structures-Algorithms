let s1 = "Hello";

// JavaScript's substring works exactly like Java's!
console.log("From 1 to 4: " + s1.substring(0, 5));

// indexOf works identically as well
console.log("Index of : " + s1.indexOf("H"));

// startsWith is built-in (Notice the camelCase just like Java)
console.log("Starts with : " + s1.startsWith("Hel"));